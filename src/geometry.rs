/// Returns a cube centered at the origin with extents `[-1.0, 1.0]` along each axis.
pub fn cube() -> [glam::Vec3; 36] {
    [
        [-1.0, -1.0, 1.0].into(),
        [1.0, -1.0, 1.0].into(),
        [1.0, 1.0, 1.0].into(),
        [-1.0, -1.0, 1.0].into(),
        [1.0, 1.0, 1.0].into(),
        [-1.0, 1.0, 1.0].into(),
        // back (-z)
        [1.0, -1.0, -1.0].into(),
        [-1.0, -1.0, -1.0].into(),
        [-1.0, 1.0, -1.0].into(),
        [1.0, -1.0, -1.0].into(),
        [-1.0, 1.0, -1.0].into(),
        [1.0, 1.0, -1.0].into(),
        // left (-x)
        [-1.0, -1.0, -1.0].into(),
        [-1.0, -1.0, 1.0].into(),
        [-1.0, 1.0, 1.0].into(),
        [-1.0, -1.0, -1.0].into(),
        [-1.0, 1.0, 1.0].into(),
        [-1.0, 1.0, -1.0].into(),
        // right (+x)
        [1.0, -1.0, 1.0].into(),
        [1.0, -1.0, -1.0].into(),
        [1.0, 1.0, -1.0].into(),
        [1.0, -1.0, 1.0].into(),
        [1.0, 1.0, -1.0].into(),
        [1.0, 1.0, 1.0].into(),
        // top (+y)
        [-1.0, 1.0, 1.0].into(),
        [1.0, 1.0, 1.0].into(),
        [1.0, 1.0, -1.0].into(),
        [-1.0, 1.0, 1.0].into(),
        [1.0, 1.0, -1.0].into(),
        [-1.0, 1.0, -1.0].into(),
        // bottom (-y)
        [-1.0, -1.0, -1.0].into(),
        [1.0, -1.0, -1.0].into(),
        [1.0, -1.0, 1.0].into(),
        [-1.0, -1.0, -1.0].into(),
        [1.0, -1.0, 1.0].into(),
        [-1.0, -1.0, 1.0].into(),
    ]
}
